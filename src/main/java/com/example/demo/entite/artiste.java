package com.example.demo.entite;

import java.sql.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;

@Entity
public class artiste {

	    @Id
	    @GeneratedValue(strategy = GenerationType.IDENTITY)
		private  Integer id  ;	
		private  Date cdate  ;
		private  String nom  ;
		private  String prenom  ;
		private  Byte[] image ;
		private  String nArtistique  ;
		private  String phone  ;
		private  String email  ;
		private  Date date  ;
		private  String nationnalite  ;
		private  String cin  ;
		private  Date datecin  ;
		private  Byte[] contrat  ;
		private  float part  ;
		private  float retenu ;
		private  String proposition ;

		
		public Integer getId() {
			return id;
		}
		public void setId(Integer id) {
			this.id = id;
		}
		public String getNom() {
			return nom;
		}
		public void setNom(String nom) {
			this.nom = nom;
		}
		public String getPrenom() {
			return prenom;
		}
		public void setPrenom(String prenom) {
			this.prenom = prenom;
		}
		public String getPhone() {
			return phone;
		}
		public void setPhone(String phone) {
			this.phone = phone;
		}
		public String getEmail() {
			return email;
		}
		public void setEmail(String email) {
			this.email = email;
		}
		public Date getDate() {
			return date;
		}
		public void setDate(Date date) {
			this.date = date;
		}
		public String getNationnalite() {
			return nationnalite;
		}
		public void setNationnalite(String nationnalite) {
			this.nationnalite = nationnalite;
		}
		public String getCin() {
			return cin;
		}
		public void setCin(String cin) {
			this.cin = cin;
		}
		public Date getDatecin() {
			return datecin;
		}
		public void setDatecin(Date datecin) {
			this.datecin = datecin;
		}
	 
		public float getPart() {
			return part; 
		}
		public void setPart(float part) {
			this.part = part;
		}
		public Date getCdate() {
			return cdate;
		}
		public void setCdate(Date cdate) {
			this.cdate = cdate;
		}
		public Byte[] getImage() {
			return image;
		}
		public void setImage(Byte[] image) {
			this.image = image;
		}
		public String getnArtistique() {
			return nArtistique;
		}
		public void setnArtistique(String nArtistique) {
			this.nArtistique = nArtistique;
		}
		public Byte[] getContrat() {
			return contrat;
		}
		public void setContrat(Byte[] contrat) {
			this.contrat = contrat;
		}

		public float getRetenu() {
			return retenu;
		}
		public void setRetenu(float retenu) {
			this.retenu = retenu;
		}
		public String getProposition() {
			return proposition;
		}
		public void setProposition(String proposition) {
			this.proposition = proposition;
		}
	 
		public artiste() {
			super();
		}
		
}
